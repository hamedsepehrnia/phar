"""
تشخیص دسته‌بندی محصولات داروخانه بر اساس نام کالا
خروجی: (دسته والد, زیردسته برگ)
"""


def classify_product(name: str) -> tuple[str, str]:
    """بر اساس نام محصول، مسیر دسته‌بندی درختی را برمی‌گرداند."""
    if not name:
        return ('سایر محصولات', 'متفرقه')

    # --- بهداشت دهان و دندان ---
    if any(k in name for k in ['مسواک', 'خميردندان', 'خمیردندان', 'زبان شور', '2080']):
        return ('بهداشت دهان و دندان', 'مسواک و خمیردندان')
    if any(k in name for k in ['دهانشويه', 'دهانشویه']):
        return ('بهداشت دهان و دندان', 'دهان‌شویه')
    if any(k in name for k in ['چسب دندان', 'خلال دندان', 'نخ دندان', 'میخچه', 'ميخچه']):
        return ('بهداشت دهان و دندان', 'لوازم جانبی دهان')

    # --- لوازم پزشکی ---
    if any(k in name for k in ['لنز', 'محلول لنز', 'اپتي فري', 'اپتی فری']):
        return ('لوازم پزشکی', 'لنز و محلول')
    if any(k in name for k in ['محلول موضع', 'اکسيليا']) and 'چشم' in name:
        return ('لوازم پزشکی', 'مراقبت چشم')
    if 'تست بارداری' in name:
        return ('لوازم پزشکی', 'تست و تشخیص')

    # --- مراقبت مو ---
    if 'شامپو' in name or 'ضدریزش' in name or 'ضدريزش' in name:
        return ('مراقبت مو', 'شامپو')
    if 'ماسک مو' in name or 'ماسک موي' in name:
        return ('مراقبت مو', 'ماسک و درمان مو')
    if 'روغن' in name:
        return ('مراقبت مو', 'روغن و تقویت مو')

    # --- مراقبت پوست ---
    if any(k in name for k in ['ضدآفتاب', 'ضدافتاب', 'ضد افتاب']):
        return ('مراقبت پوست', 'ضدآفتاب')
    if any(k in name for k in ['دورچشم', 'دور چشم']):
        return ('مراقبت پوست', 'مراقبت دور چشم')
    if 'سرم' in name:
        return ('مراقبت پوست', 'سرم')
    if any(k in name for k in ['میسلار', 'ميسلار', 'تونر', 'شوينده', 'شوینده', 'پاک کن', 'پاک‌کن', 'فوم']):
        return ('مراقبت پوست', 'پاک‌کننده و تونر')
    if 'محلول' in name and any(k in name for k in ['آرایش', 'ارايش', 'دو فاز']):
        return ('مراقبت پوست', 'پاک‌کننده و تونر')
    if 'سوختگ' in name:
        return ('مراقبت پوست', 'درمان و بهداشت پوست')
    if any(k in name for k in ['کرم', 'ژل', 'لوسيون', 'لوسیون', 'پماد', 'ابرسان', 'مرطوب', 'پن ']):
        return ('مراقبت پوست', 'کرم و مرطوب‌کننده')

    # --- بهداشت شخصی ---
    if 'صابون' in name:
        return ('بهداشت شخصی', 'صابون و شستشوی بدن')
    if any(k in name for k in ['دئودورانت', 'دئودور', 'رکسونا', 'rexona']):
        return ('بهداشت شخصی', 'دئودورانت')
    if any(k in name for k in ['تیغ', 'تيغ', 'ژيلت', 'موچین', 'موچين', 'اصلاح', 'بيک']):
        return ('بهداشت شخصی', 'اصلاح و پیرایش')
    if any(k in name for k in ['اسپری', 'اسپري', 'اسپلش', 'بادی', 'استیک', 'استيک', 'ماست بدن', 'عطر', 'اینتیم', 'اینتيم']):
        return ('بهداشت شخصی', 'اسپری و خوشبوکننده')
    if 'افترشيو' in name or 'aftershave' in name.lower():
        return ('بهداشت شخصی', 'افترشیو')
    if any(k in name for k in ['پد زیر', 'پد ضد', 'دورامنس', 'کاندوم', 'چرچيلز']):
        return ('بهداشت شخصی', 'بهداشت زنان و بزرگسال')
    if any(k in name for k in ['حشرات', 'دافع']):
        return ('بهداشت شخصی', 'محافظت و مراقبت')

    # --- آرایشی ---
    if any(k in name for k in ['رژ', 'ریمل', 'کرم پودر', 'بی بی', 'بي بي', 'بالم لب', 'آرایش', 'آرايش', 'خط چشم']):
        return ('آرایشی', 'لوازم آرایشی')

    # --- مکمل و ویتامین ---
    if any(k in name for k in ['اسلیم', 'اسليم', 'برنينگ', 'slim']):
        return ('مکمل و ویتامین', 'لاغری و چربی‌سوزی')
    if 'کارنيتين' in name or 'کارنیتین' in name:
        return ('مکمل و ویتامین', 'لاغری و چربی‌سوزی')
    if any(k in name for k in ['امگا', 'omega']):
        return ('مکمل و ویتامین', 'امگا ۳ و روغن‌های مفید')
    if any(k in name for k in ['کلاژن', 'هیالورون', 'هيالورون', 'گلوتاتیون', 'collagen']):
        return ('مکمل و ویتامین', 'مکمل پوست و زیبایی')
    if any(k in name for k in ['پروبیوتیک', 'پروبيوتيک', 'گوارش', 'IBS', 'پدي', 'دايجست']):
        return ('مکمل و ویتامین', 'مکمل گوارشی')
    if 'رزماری' in name or ('محلول' in name and 'باريج' in name):
        return ('مکمل و ویتامین', 'مکمل گیاهی')

    if any(k in name for k in ['کپسول', 'قرص', 'شربت', 'قطره', 'ساشه', 'آمپول', 'پودر', 'ويال', 'پرل', 'مکمل', 'انشور']):
        if any(k in name for k in ['کودک', 'کيدز', 'کیندر', 'کيندر', 'نوزاد', 'بارداری', 'باردار', 'kids']):
            return ('مکمل و ویتامین', 'مکمل کودک')
        if any(k in name for k in ['لاغری', 'لاغري', 'کاهش وزن', 'چربی', 'متاب']):
            return ('مکمل و ویتامین', 'لاغری و چربی‌سوزی')
        if any(k in name for k in ['ویتامین', 'ويتامين', 'مولتی', 'مولتي', 'زینک', 'زينک', 'منیزیم', 'منيزيم', 'کلسیم', 'کلسيم', 'آهن', 'بیوتین', 'بیوتين', 'b12', 'b6', 'd3', 'd2', 'a+d', 'اي400', 'اي 200', 'اي200']):
            return ('مکمل و ویتامین', 'ویتامین و مواد معدنی')
        return ('مکمل و ویتامین', 'مکمل عمومی')

    if any(k in name for k in ['ویتامین', 'ويتامين', 'زینک', 'زينک', 'منیزیم', 'منيزيم', 'کلسیم', 'کلسيم']):
        return ('مکمل و ویتامین', 'ویتامین و مواد معدنی')

    return ('سایر محصولات', 'متفرقه')


